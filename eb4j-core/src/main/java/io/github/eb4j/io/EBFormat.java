package io.github.eb4j.io;

/**
 * EBook Format types.
 * Created by Hiroshi Miura on 16/07/02.
 * @author Hiroshi Miura
 */
public enum EBFormat {
    /**
     * PLAIN形式
     */
    FORMAT_PLAIN,
    /**
     * EBZIP形式
     */
    FORMAT_EBZIP,
    /**
     * EPWING V4/V5形式
     */
    FORMAT_EPWING,
    /**
     * EPWING V6形式
     */
    FORMAT_EPWING6,
    /**
     * S-EBXA形式
     */
    FORMAT_SEBXA,
    /**
     * Unkown
     */
    FORMAT_UNKNOWN
}
